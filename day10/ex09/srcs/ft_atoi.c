int			ft_atoi(char *str)
{
	int n;
	int sign;

	n = 0;
	while (*str == '\t' || *str == '\n' || *str == ' '
			|| *str == '\v' || *str == '\f' || *str == '\r')
		++str;
	sign = 1;
	if (*str == '-')
	{
		sign = -1;
		++str;
	}
	else if (*str == '+')
		++str;
	while (*str >= '0' && *str <= '9')
	{
		n = n * 10 + (*str - '0');
		++str;
	}
	return (n * sign);
}
