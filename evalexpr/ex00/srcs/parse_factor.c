#include "ft.h"

int		parse_factor(void)
{
	int x;

	x = 0;
	if (*g_x <= '9' && *g_x >= '0')
		while (*g_x <= '9' && *g_x >= '0')
		{
			x = x * 10 + (*g_x - '0');
			g_x++;
		}
	else
	{
		g_x++;
		x = parse_sum();
		g_x++;
	}
	return (x);
}
